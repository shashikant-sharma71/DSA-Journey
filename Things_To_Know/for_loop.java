
import java.util.*;
public class for_loop {
    public static void main(String[] args){
        Scanner sc= new Scanner(System.in);

        for(int i=5 ; i>=0 ; i--){
            System.out.println("Hello World"+i);

        }
   sc.close ();






   int i=1;
   while(i<=5){
    System.out.println("Hello World");
    i++;
   }

    }
    
}


